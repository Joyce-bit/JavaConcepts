package org.primitivedatatype;

public class PrimitiveDataType {
	
	public static void main (String args []) {
		
			// primitive data types
		
			byte age = 21;				
			short pinNo = 12345;
			int regNo = 1234567890;
			long phone = 987654321;
			float salary = 72000.00f;
			double 	gst = 1234.37;
			boolean result = true;
			char gender = 'F';
			
			// + operator is used to concatenate string and variable
			
			System.out.println("Age is : " + age);
			System.out.println("Pin number is : " + pinNo);
			System.out.println("Registration number is : " + regNo);
			System.out.println("Phone Number is : " + phone);
			System.out.println("Salary is : " + salary );
			System.out.println("GST is : " + gst);
			System.out.println("Result is : " + result);
			System.out.println("Gender is : " + gender);
			
		
	}
	

}
